package pink;

import clojure.lang.IFn;

public class Utils {

  /* from Zach Tellman's Primitive Math library
   * https://github.com/ztellman/primitive-math 
   */
  public static boolean neq(double a, double b) {
    return a != b;
  }

  /* from Zach Tellman's Primitive Math library
   * https://github.com/ztellman/primitive-math 
   */
  public static boolean neq(long a, long b) {
    return a != b;
  }

}
